	public ShellbuncleEntity(EntityType<ShellbuncleEntity> type, Level world) {
		super(type, world);
		xpReward = 3;
		setNoAi(false);
		this.setPathfindingMalus(PathType.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (ShellbuncleEntity.this.isInWater())
					ShellbuncleEntity.this.setDeltaMovement(ShellbuncleEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !ShellbuncleEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - ShellbuncleEntity.this.getX();
					double dy = this.wantedY - ShellbuncleEntity.this.getY();
					double dz = this.wantedZ - ShellbuncleEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * ShellbuncleEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					ShellbuncleEntity.this.setYRot(this.rotlerp(ShellbuncleEntity.this.getYRot(), f, 10));
					ShellbuncleEntity.this.yBodyRot = ShellbuncleEntity.this.getYRot();
					ShellbuncleEntity.this.yHeadRot = ShellbuncleEntity.this.getYRot();
					if (ShellbuncleEntity.this.isInWater()) {
						ShellbuncleEntity.this.setSpeed((float) ShellbuncleEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						ShellbuncleEntity.this.setXRot(this.rotlerp(ShellbuncleEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(ShellbuncleEntity.this.getXRot() * (float) (Math.PI / 180.0));
						ShellbuncleEntity.this.setZza(f3 * f1);
						ShellbuncleEntity.this.setYya((float) (f1 * dy));
					} else {
						ShellbuncleEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					ShellbuncleEntity.this.setSpeed(0);
					ShellbuncleEntity.this.setYya(0);
					ShellbuncleEntity.this.setZza(0);
				}
			}
		};
	}

